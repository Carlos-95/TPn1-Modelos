    public int asignarDineroJugadores(int dineroInicial, Jugador unJugador, int cantidadJugadoresObtenida){
        int valorMinimo = cantidadJugadoresObtenida - unJugador.getId();
        int montoParaAsignar = this.miVisual.obtenerDineroParaJugador(dineroInicial-valorMinimo, /*cantidadJugadoresObtenida*/0);//si cantidadMinima es mayor que 0 entonces comienzan a aparecer negativos
//        if(montoParaAsignar>=2048){
            unJugador.setSaldoDisponible(montoParaAsignar);
            dineroInicial -=montoParaAsignar; 
        return dineroInicial;
    }

    public void cargarTablaBolillero(){
        String cabecera[] = {"No Estandarizado", "Estandarizado"};
        String[][] datos = {};
        tablaBolillero = new DefaultTableModel(datos, cabecera);
        tblBolillero.setModel(tablaBolillero);
        for(Double unDouble : this.numerosGeneradoPorUsuario){
            
            Object datosRow[] = {unDouble, unDouble/Integer.parseInt(this.txtModulo.getText())};
            this.tablaBolillero.addRow(datosRow);
        }
    }
    
    public void cargarTablaJugadores(){
        
        String cabecera[] = {"Numero Jugador" , "Saldo"};
        String[][] datos = {};
        tablaJugadores = new DefaultTableModel(datos, cabecera);
        tblJugadores.setModel(tablaJugadores);
        for(Jugador unJugador : jugadores/*this.miVisual.dameTodosLosJugadores()*/){
            
            Object datosRow[] = {unJugador.getId(), unJugador.getSaldoDisponible()};
            this.tablaJugadores.addRow(datosRow);
        }
    }
    
    public void iniciarTablaValoresBolillero(){
        String cabecera[] = {"Xo", "m", "a", "c"};
        String[][] datos = {};
        boolean bandera = true;
        tablaValoresBolillero = new DefaultTableModel(datos, cabecera);
        tblValoresBolillero.setModel(tablaValoresBolillero);
        for(Double unDouble : this.numerosGeneradoPorUsuario){
            if(bandera){
                bandera=false;
                Object datosRow[] = {this.txtSemilla.getText(), this.txtModulo.getText(),this.txtMultiplicador.getText(),this.txtIncremento.getText()};
                this.tablaValoresBolillero.addRow(datosRow);
                Object datosRow2[] = {unDouble, this.txtModulo.getText(),this.txtMultiplicador.getText(),this.txtIncremento.getText()};
                this.tablaValoresBolillero.addRow(datosRow2);
            }else{
                Object datosRow[] = {unDouble, this.txtModulo.getText(),this.txtMultiplicador.getText(),this.txtIncremento.getText()};
                this.tablaValoresBolillero.addRow(datosRow);
            }
            
        }
        
        
    }
