    private void btnRepartirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRepartirMouseClicked
        int numeroJugadoresMin, numeroJugadoresMax, dineroInicial;
        int cantidadJugadoresObtenida;
        numeroJugadoresMin = Integer.parseInt(this.txtNroJugadoresMin.getText());
        numeroJugadoresMax = Integer.parseInt(this.txtNroJugadoresMax.getText());
        dineroInicial = Integer.parseInt(this.txtDineroInicial.getText());
        
        cantidadJugadoresObtenida = this.miVisual.obtenerEnteroEntreDosNumeros(numeroJugadoresMax, numeroJugadoresMin);
        this.lblPrueba1.setText(String.valueOf(cantidadJugadoresObtenida));
        
        
        this.jugadores = this.miVisual.crearJugadoresRonda(cantidadJugadoresObtenida);
        for (Jugador unJugador : jugadores) {
            if(unJugador.getId() == cantidadJugadoresObtenida && dineroInicial>=0 ){
                unJugador.setSaldoDisponible(dineroInicial);
            }else{
                dineroInicial = this.asignarDineroJugadores(dineroInicial, unJugador, cantidadJugadoresObtenida);
            }
        }
        this.miVisual.generarApuestasJugador();
        this.cargarTablaJugadores();
        
    }//GEN-LAST:event_btnRepartirMouseClicked

